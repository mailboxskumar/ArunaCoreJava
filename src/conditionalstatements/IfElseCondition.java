package conditionalstatements;

public class IfElseCondition {

	public static void main(String[] args) {

		// if-else conditions
		
		int a = 1;
		int b = 2;

		if (a > b) {
			System.out.println("a is greater then b");
		} else {
			System.out.println("a is less then b");
		}

	}

}
