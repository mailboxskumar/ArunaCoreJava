package conditionalstatements;

public class IfConditiona {

	public static void main(String[] args) {

		// if conditions
		
		int a = 1;
		int b = 2;

		if (a > b) {
			System.out.println("a is greater then b");
		}

		if (b > a) {
			System.out.println("b is greater then a");
		}


	}

}
